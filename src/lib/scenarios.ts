export const SCENARIO_IDS = SCENARIOS.map((s) => s.id) as [string, ...string[]];

export function transcriptText(messages: Array<{ role: string; text: string }>): string {
  return messages
    .map((m) => (m.role === "staff" ? `【営業】${m.text}` : `【客】${m.text}`))
    .join("\n");
}

export function buildCustomerPrompt(scenarioId: string, messages: Array<{ role: string; text: string }>): string {
  const brief = SCENARIO_BRIEFS[scenarioId];
  return `あなたは携帯販売の営業ロープレ研修ツールの「客役AI」です。リアルな客を演じてください。

【シナリオ設定】
${brief}

【演技ルール】
- 現実の客として振る舞う。営業に都合よく協力しない。良いトークの途中でも、リアルなら立ち去る場面では立ち去る。
- 営業の「技術」にのみ反応する。努力や熱意ではなく、引きの姿勢・正直さ・事実提示・筋の通った手続きに警戒度を下げる。
- 即決圧、精神論、盛った数字、答え漏れ、過剰テンション、相手に合わない専門用語には警戒度を上げる。
- 口調・感情・仕草(括弧書き)を含めてリアルに描写する。返答は日本語で3〜6文程度。
- 営業が決定的に信頼を獲得し契約・解決に向かう流れが確定したら status を "win" に。客が完全に離脱・破談したら "lose" に。それ以外は "continue"。
- 開始から10往復を超えたら、決着がつく方向に会話を収束させること。

【これまでの会話】
${transcriptText(messages) || "(まだ会話なし。あなたの最初の登場シーンと第一声を出力してください)"}

【出力形式】
以下のJSONのみを出力。他のテキストは一切含めない。
{"reply": "客のセリフと仕草(括弧書き含む)", "guard": 警戒度0-100の整数, "status": "continue" または "win" または "lose"}`;
}

