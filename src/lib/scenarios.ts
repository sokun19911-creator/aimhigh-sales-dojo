export function buildObjectionPrompt(
  scenarioId: string,
  messages: Array<{ role: string; text: string }>,
  scoring: { score: number; deductions: Array<{ points: number; reason: string }> },
  objection: string
): string {
  return `あなたはロープレ研修の採点官です。受講者から採点への反論が届きました。
言い訳には一切応じませんが、論理的に正しい反論には点数を修正します。

${RUBRIC}

【ロープレ記録】
${transcriptText(messages)}

【現在の採点】
点数: ${scoring.score}
減点内容: ${scoring.deductions.map((d) => `-${d.points}点: ${d.reason}`).join(" / ")}

【受講者の反論】
${objection}

【判定指示】
- 反論が営業判断として論理的に正しいなら減点を一部または全部取り消して点数を上げる。
- 単なる言い訳・結果論なら点数を維持し、理由を明確に伝える。
- 部分的に正しい場合は部分的に修正する。

【出力形式】
以下のJSONのみを出力。
{"accepted": true または false, "new_score": 整数, "verdict": "判定理由を2〜3文で"}`;
}
